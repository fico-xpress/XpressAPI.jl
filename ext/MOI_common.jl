function MOI.supports(model::Optimizer, ::MOI.NumberOfConstraints{F,S})::Bool where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    return true
end
function MOI.get(model::Optimizer, ::MOI.NumberOfConstraints{F,S})::Int64 where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    _F = MOI_precision_type(F)
    _S = MOI_precision_type(S)
    if !haskey(model.constraints, _F)
        return 0
    end
    if !haskey(model.constraints[_F], _S)
        return 0
    end
    return length(model.constraints[_F][_S])
end

