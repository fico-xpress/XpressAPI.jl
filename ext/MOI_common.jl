            solstatus, solvec = XPRSgetduals(model.inner, c.solbuffer, c.xprs_index, c.xprs_index)
            if solstatus == XPRS_SOLAVAILABLE_NOTFOUND
                return NaN
            end
            return solvec[1]
        end,
        indices
    )
