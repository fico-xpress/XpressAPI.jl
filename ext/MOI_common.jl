function _base_type(::Type{T}) where {T}
    try
        return T.name.wrapper
    catch e
        return T
    end
end

function MOI_precision_type(::Type{T}) where {T}
    # Those MOI types have a template parameter for number type
    # return templated parameters with precision P !
    if T <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo, MOI.Interval, MOI.ScalarAffineFunction, MOI.ScalarQuadraticFunction, MOI.VectorAffineFunction, MOI.VectorQuadraticFunction}
        return _base_type(T){Precision}
    end
    # Else, return untemplated original type !
    return T
end

