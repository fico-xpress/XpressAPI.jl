    _F = MOI_precision_type(F)
    _S = MOI_precision_type(S)
    return haskey(model.constraints, _F) && haskey(model.constraints[_F], _S) && haskey(model.constraints[_F][_S], MOI.ConstraintIndex{_F,_S}(index.value))
