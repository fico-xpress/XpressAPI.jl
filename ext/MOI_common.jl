function MOI.set(model::Optimizer, p::MOI.RawOptimizerAttribute, value)
    # Only controls can be set in Xpress
    try
        XPRSsetcontrol(model.inner, p.name, value)
    catch ex
        throw(MOI.SetAttributeNotAllowed(p, ex.message))
    end
end
function MOI.get(model::Optimizer, p::MOI.RawOptimizerAttribute)
    # Get attribute OR control value from Xpress
    try
        return XPRSgetattrib(model.inner, p.name)
    catch exattrib
        try
            return XPRSgetcontrol(model.inner, p.name)
        catch excontrol
            throw(MOI.GetAttributeNotAllowed(p, "Unknown Xpress attribute or control $(p.name)"))
        end
    end
end


