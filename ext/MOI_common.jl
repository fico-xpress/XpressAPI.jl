function MOI.add_variables(model::Optimizer, n::Int)::Vector{MOI.VariableIndex}
    _XPRS_throw_if_nothing(model)
    old_n = length(model.variables)
    indices = map(
        # Generate new MOI.VariableIndex
        i -> begin
            # Generate MOI index by adding dummy variable
            index = MOI.Utilities.CleverDicts.add_item(
                model.variables,
                Variable(0, 0)
            )
            # Now set variable info
            v = _XPRS_variable_from_moi_index(model, index)
            v.moi_index = index.value
            v.xprs_index = old_n + (i-1)
            return index
        end,
        # Range
        1:n
    )

    # Fill Xpress matrix
    XPRSaddcols64(
        model.inner,
        n,
        0, # Assume variables are not in any constraints thus far
        zeros(n), # Assume variable coefs are 0.0 in objective
        nothing,
        nothing,
        nothing,
        nothing,
        nothing
    )
    return indices
end

