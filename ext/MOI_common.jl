# Floating point precision of a given model
const Precision = Float64

# Struct to map MOI variable indexes to Xpress column indexes
mutable struct Variable
    # Index in the MOI/JuMP model
    moi_index::Int

    # Index in the Xpress problem/matrix (col number)
    xprs_index::Int

    # Buffer to avoid allocation when retrieving solution from Xpress
    solbuffer::Array{Precision, 1}

    # Initial Values, useful for warmstart
    primal_start::Union{Precision, Nothing}
    dual_start::Union{Precision, Nothing}

    function Variable(moi_index::Int, xprs_index::Int)
        return new(
            moi_index,
            xprs_index,
            Precision[NaN],
            nothing,
            nothing
        )
    end
end


mutable struct Constraint
    moi_index::Int

    # Index in the Xpress problem/matrix (row number)
    xprs_index::Int

    # Store function type for problem type detection
    # A Constraint can have multiple types
    # For example, when adding a Quadratic constraint, Linear constraint 0x + 0 = 0 is added
    # Thus, this constraint is Linear AND quadratic
    # Cones are added as Quadratic constraints, they will have types [Linear, Quadratic, Cone]
    moi_fct_type::Type{<: MOI.AbstractFunction}
    moi_set_type::Type{<: MOI.AbstractSet}
    moi_fct_value::MOI.AbstractFunction
    moi_set_value::MOI.AbstractSet

    # Buffer to avoid allocation when retrieving solution from Xpress
    solbuffer::Array{Precision, 1}

    # If the constraint generated subconstraints
    # Like VectorAffineFunction actually generates a set of ScalarAffineFunction
    subconstraints::Vector{Constraint}

    function Constraint(
        moi_index::Int,
        xprs_index::Int,
        moi_fct_type::Type{<: MOI.AbstractFunction},
        moi_set_type::Type{<: MOI.AbstractSet},
        moi_fct_value::MOI.AbstractFunction,
        moi_set_value::MOI.AbstractSet
)
        return new(
            moi_index,
            xprs_index,
            moi_fct_type,
            moi_set_type,
            moi_fct_value,
            moi_set_value,
            Precision[NaN],
            Vector{Constraint}()
        )
    end
end

mutable struct IISData
    iisnumber::Int
    status::Int32
    nrows::Int
    ncols::Int
    rowind
    colind

    function IISData(iisnumber::Int, status::Int32)
        return new(iisnumber, status, -1, -1, zeros(Int32, 0), zeros(Int32, 0))
    end
end

mutable struct Optimizer <: MOI.AbstractOptimizer
    # Actual handle on the low-level Xpress problem
    inner::Union{XPRSprob, Nothing}

    # Optimization sense
    sense::MOI.OptimizationSense

    # Storage for variables
    variables::MOI.Utilities.CleverDicts.CleverDict{
        MOI.VariableIndex,
        Variable,
        typeof(MOI.Utilities.CleverDicts.key_to_index),
        typeof(MOI.Utilities.CleverDicts.index_to_key)
    }

    # Last known solve status
    last_solve_status::Int32

    # Last known solution status
    last_solution_status::Int32

    # Storage for constraints
    # `constraints` is lazily set in _XPRS_register_new_constraint_as
    constraints::Dict

    # List of objective functions
    objectives::Vector{MOI.AbstractFunction}

    # Non-linear model, useful to store user-defined operators
    # See JuMP.@operator
    nlp_model::Union{Nothing, MOI.Nonlinear.Model}

    # Mapping user-defined function Symbol to their Xpress token_id
    user_func_mapping::Dict{Symbol, Int}

    # List of IIS structures found so far
    iisdata::Vector{IISData}

    # Is the model MIP ?
    has_integer_variables::Bool

    function Optimizer(; name::String = "", kwargs...)
        model = new(
            XPRScreateprob(name),
            MOI.FEASIBILITY_SENSE,
            MOI.Utilities.CleverDicts.CleverDict{MOI.VariableIndex, Variable}(),
            XPRS_SOLVESTATUS_UNSTARTED,
            XPRS_SOLSTATUS_NOTFOUND,
            # `constraints` is lazily set in _XPRS_register_new_constraint_as
            Dict(),
            # Objectives
            Vector{MOI.AbstractFunction}(),
            nothing,
            Dict{Symbol, Int}(),
            Vector{IISData}(),
            false
        )

        # MOI Advises for solvers to have verbosity ON by default
        # https://jump.dev/MathOptInterface.jl/stable/reference/models/#MathOptInterface.Silent
        XPRSsetcontrol(model.inner, "OUTPUTLOG", XPRS_OUTPUTLOG_FULL_OUTPUT)
        # Print outputs
        XPRSaddcbmessage(model.inner, (cbprob, msg, msglen, msgtype) -> if msglen > 0 println(msg); end, 0)
        return model
    end
end

function is_mip(model::Optimizer)::Bool
    # TODO: Check if model is LP relaxed
    return model.has_integer_variables
end

function _initialize_nlp(model::Optimizer)
    if isnothing(model.nlp_model)
        model.nlp_model = MOI.Nonlinear.Model()
    end
    return nothing
end

function _XPRS_is_model_nothing(model::Optimizer)::Bool
    return isnothing(model) || isnothing(model.inner)
end

# Sanity check on inner problem
function _XPRS_throw_if_nothing(model::Optimizer)
    if _XPRS_is_model_nothing(model)
        throw(MOI.AddVariableNotAllowed("Variable cannot be added in void solver"))
    end
end

function MOI.is_empty(model::Optimizer)::Bool
    return _XPRS_is_model_nothing(model) ||
        isempty(model.variables)
end

function MOI.empty!(model::Optimizer)
    if MOI.is_empty(model)
        return
    end
    # Empty containers
    empty!(model.variables)
    empty!(model.constraints)
    if !isnothing(model.nlp_model)
        empty!(model.nlp_model)
    end
    empty!(model.user_func_mapping)
    empty!(model.iisdata)
    # Reset states
    model.last_solve_status = XPRS_SOLVESTATUS_UNSTARTED
    model.last_solution_status = XPRS_SOLSTATUS_NOTFOUND
    model.has_integer_variables = false
    # Load an empty matrix into the inner problem
    XPRSloadlp(
        model.inner,
        nothing, 0, 0, nothing, nothing, nothing,
        nothing, nothing, nothing, nothing, nothing, nothing,
        nothing
    )
    return
end

