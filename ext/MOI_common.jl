    if !MOI.is_valid(model, index)
        throw(MOI.InvalidIndex(index))
    end

    # Here 'set' is generic and might be MOI.LessThan{Int64} (if ints where used when writting the constraint) when we actually want MOI.LessThan{P}
    # Same goes for 'func', so we need to convert template type to 'P' as we only store constraints for type 'P'
    _F = MOI_precision_type(F)
    _S = MOI_precision_type(S)
    return model.constraints[_F][_S][MOI.ConstraintIndex{_F,_S}(index.value)]
end

# Given a Constraint, register in as a new index
function _XPRS_register_new_constraint_as(model::Optimizer, c::Constraint, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    # Here 'set' is generic and might be MOI.LessThan{Int64} (if ints where used when writting the constraint) when we actually want MOI.LessThan{P}
    # Same goes for 'func', so we need to convert template type to 'P' as we only store constraints for type 'P'
    _F = MOI_precision_type(F)
    _S = MOI_precision_type(S)

    # If this is a new type of constraint to register, lazily create a new entry
    if !haskey(model.constraints, _F)
        model.constraints[_F] = Dict()
    end
    if !haskey(model.constraints[_F], _S)
        model.constraints[_F][_S] = MOI.Utilities.CleverDicts.CleverDict{
            MOI.ConstraintIndex{_F, _S},
            Constraint
        }()
    end
    index = MOI.Utilities.CleverDicts.add_item(model.constraints[_F][_S], c)

    # Set the new type of the created constraint object
    c.moi_fct_type = F
    c.moi_set_type = S
    c.moi_fct_value = func
    c.moi_set_value = set
    # MOI requires us to return actual index for {F,S} without type {P} specifically
    return MOI.ConstraintIndex{F,S}(index.value)
end

# Add a new constraint and generate a MOI index for it
function _XPRS_register_new_constraint(model::Optimizer, xprs_index::Int64, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    c = Constraint(0, xprs_index, F, S, func, set)
    # Generate the MOI index for the new constraint
    new_index = _XPRS_register_new_constraint_as(model, c, func, set)
    # Set the new index to the constraint
    c.moi_index = new_index.value
    return new_index
end

# Register a new constraint as 'parent' of the given constraints
# This means the constraint generated the given constraints
# Like VectorAffineFunction actually generates a set of ScalarAffineFunction
function _XPRS_register_new_constraint_as_parent(
    model::Optimizer,
    func::F, set::S,
    childs::Vector{<: MOI.ConstraintIndex}
) where {
    F <: MOI.AbstractFunction, S <: MOI.AbstractSet
}
    # Parent constraints are not actually loaded into Xpress, only its subconstraints are
    # We set then xprs_index = -2
    new_index = _XPRS_register_new_constraint(model, -2, func, set)
    new_c = _XPRS_constraint_from_moi_index(model, new_index)
    # Set constraint subfunction
    new_c.subconstraints = map(c -> _XPRS_constraint_from_moi_index(model, c), childs)
    return new_index
end


function MOI.get(model::Optimizer, result_index::MOI.ConstraintPrimal, indices::Vector{MOI.ConstraintIndex{F,S}}) where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    @assert result_index.result_index == 1
    return map(index -> begin
            c = _XPRS_constraint_from_moi_index(model, index)
            if length(c.subconstraints) > 0
                return map(subc -> MOI.get(model, result_index, subc.moi_index), c.subconstraints)
            end

            # Variable bounds and SOS constraints are registered with xprs_index = -2
            if c.xprs_index == -2
                if c.moi_set_type <: Union{MOI.SOS1, MOI.SOS2}
                    throw(MOI.GetAttributeNotAllowed(result_index, "Cannot retrieve `ConstraintPrimal` for SOS constraints"))
                end
                # For variable bound constraints, return NaN
                return [NaN]
            end

            # Get the slack variable value associated with the constraint
            solstatus, solvec = XPRSgetslacks(model.inner, c.solbuffer, c.xprs_index, c.xprs_index)
            if solstatus == XPRS_SOLAVAILABLE_NOTFOUND
                return [NaN]
            end
            slack = solvec[1]
            # Get the rhs of the constraint to evaluate the constraint
            rhs = XPRSgetrhs(model.inner, c.solbuffer, c.xprs_index, c.xprs_index)[1]
            if S <: MOI.EqualTo
                return Precision(rhs)
            end

            if S <: MOI.GreaterThan
                return Precision(rhs - slack)
            end

            if S <: MOI.LessThan
                return Precision(rhs + slack)
            end
            # Can't evaluate constraint
            return [NaN]
        end,
        indices
    )
    return solvec
end

function MOI.get(model::Optimizer, result_index::MOI.ConstraintPrimal, index::MOI.ConstraintIndex{F,S}) where {
        F <: MOI.AbstractFunction, S <: MOI.AbstractSet
    }
    return MOI.get(model, result_index, [index])[1]
end

