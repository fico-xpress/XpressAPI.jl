# Solve Status -> Reason -> (MOI.TerminationStatusCode, "String message")
const XPRS_STATUS_MAPPING = Dict(
    XPRS_SOLVESTATUS_UNSTARTED => Dict(
        :default => (MOI.OPTIMIZE_NOT_CALLED, "The solve has not been started.")
    ),
    XPRS_SOLVESTATUS_STOPPED => Dict(
        :default => (MOI.INTERRUPTED, "Optimization has been interrupted."),
        # Callback to retrieve reason value of `XPRS_SOLVESTATUS_STOPPED`
        :reason => model -> XPRSStopType(MOI.get(model, MOI.RawOptimizerAttribute("STOPSTATUS"))),
        # Reasons values (type XPRSStopType)
        XPRS_STOP_NONE => (MOI.OTHER_ERROR, "Solve stopped for unknown reason(s)."),
        XPRS_STOP_TIMELIMIT => (MOI.TIME_LIMIT, "Solve stopped after a user-specified computation time."),
        XPRS_STOP_CTRLC => (MOI.INTERRUPTED, "Solve stopped by user input: CTRL+C."),
        XPRS_STOP_NODELIMIT => (MOI.NODE_LIMIT, "Maximum number of nodes has been reached."),
        XPRS_STOP_ITERLIMIT => (MOI.ITERATION_LIMIT, "An iterative algorithm stopped after conducting the maximum number of iterations."),
        XPRS_STOP_MIPGAP => (MOI.OTHER_LIMIT, "User-defined MIP gap was reached."),
        XPRS_STOP_SOLLIMIT => (MOI.SOLUTION_LIMIT, "Solve stopped because it found the required number of solutions."),
        XPRS_STOP_GENERICERROR => (MOI.OTHER_ERROR, "Solve stopped for unknown reason(s)."),
        XPRS_STOP_MEMORYERROR => (MOI.MEMORY_LIMIT, "Solve stopped because it ran out of memory."),
        XPRS_STOP_USER => (MOI.INTERRUPTED, "Solve stopped by user condition(s)."),
        XPRS_STOP_SOLVECOMPLETE => (MOI.LOCALLY_SOLVED, "Solve terminated without errors."),
        XPRS_STOP_LICENSELOST => (MOI.OTHER_ERROR, "Xpress license was lost during the solve."),
        XPRS_STOP_NUMERICALERROR => (MOI.NUMERICAL_ERROR, "Solve stopped because it encountered unrecoverable numerical error."),
        XPRS_STOP_WORKLIMIT => (MOI.OTHER_LIMIT, "Solve stopped because it reached the work limit."),
        XPRS_STOP_NEXTOBJECTIVE => (MOI.LOCALLY_SOLVED, "Solve reached next objective target."),
    ),
    XPRS_SOLVESTATUS_FAILED => Dict(
        :default => (MOI.OTHER_ERROR, "Optimization has run into a nonrecoverable problem and failed."),
    ),
    XPRS_SOLVESTATUS_COMPLETED => Dict(
        :default => (MOI.OTHER_ERROR, "Optimization finished without errors."),
        # Callback to retrieve reason value of `XPRS_SOLVESTATUS_COMPLETED`
        :reason => model -> XPRSSolStatus(model.last_solution_status),
        # Reasons values (type XPRSSolStatus)
        XPRS_SOLSTATUS_NOTFOUND => (MOI.OTHER_ERROR , "No solution could be found."),
        XPRS_SOLSTATUS_OPTIMAL => (MOI.OPTIMAL, "Problem solved to optimality."),
        XPRS_SOLSTATUS_FEASIBLE => (MOI.LOCALLY_SOLVED, "Found a feasible solution."),
        XPRS_SOLSTATUS_INFEASIBLE => (MOI.INFEASIBLE, "Problem is infeasible."),
        XPRS_SOLSTATUS_UNBOUNDED => (MOI.DUAL_INFEASIBLE, "Problem is unbounded."),
    )
)

function get_last_solve_status_MOI(model::Optimizer)::Tuple{MOI.TerminationStatusCode, String}
    last_solve_status::XPRSSolveStatus = XPRSSolveStatus(model.last_solve_status)
    if haskey(XPRS_STATUS_MAPPING, last_solve_status)
        reason = :default
        if haskey(XPRS_STATUS_MAPPING[last_solve_status], :reason)
            # Call callback to get reason for `model.last_solve_status`
            reason = XPRS_STATUS_MAPPING[last_solve_status][:reason](model)
        end
        moi_code, str_message = XPRS_STATUS_MAPPING[last_solve_status][reason]
        return moi_code, str_message
    end
    # Default
    return MOI.OTHER_ERROR, "Unknown solve error."
end


