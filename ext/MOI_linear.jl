# --------------- MathOptInterface.Objective ---------------

# Scalar objective
function MOI.supports(::Optimizer, ::MOI.ObjectiveFunction{F})::Bool where {
        F <: Union{MOI.VariableIndex, MOI.ScalarAffineFunction}
    }
    return true
end

function _add_linear_objective(model::Optimizer, objidx::Int, func::MOI.ScalarAffineFunction)
    if length(func.terms) > 0
        XPRSchgobjn(
            model.inner,
            objidx,
            length(func.terms),
            map(t -> _XPRS_variable_from_moi_index(model, t.variable).xprs_index, func.terms),
            map(t -> Precision(t.coefficient), func.terms)
        )
    end

    # Set objective's constant
    XPRSsetobjdblcontrol(model.inner, objidx, XPRS_OBJECTIVE_RHS, func.constant)
    push!(model.objectives, func)
    return
end

# Linear objective R^n -> R
function MOI.set(model::Optimizer, ::MOI.ObjectiveFunction{F}, set::F) where {
        F <: MOI.ScalarAffineFunction
    }
    # Add as objective '0' -> single objective
    _add_linear_objective(model, 0, set)
    # Return dummy index
    return MOI.VariableIndex(-1)
end

# Single variable objective x -> 1.0 * x + 0.0
function MOI.set(model::Optimizer, ::MOI.ObjectiveFunction{F}, set::F) where {
        F <: MOI.VariableIndex
    }
    t::MOI.ScalarAffineTerm{Precision} = MOI.ScalarAffineTerm(1.0, set)
    f::MOI.ScalarAffineFunction{Precision} = MOI.ScalarAffineFunction([t], 0.0)
    return MOI.set(model, MOI.ObjectiveFunction{MOI.ScalarAffineFunction{Precision}}(), f)
end



# --------------- MathOptInterface.Constraints --------------
# https://jump.dev/MathOptInterface.jl/stable/manual/constraints/#Linear-constraints

function MOI.supports_constraint(model::Optimizer, ::Type{F}, ::Type{S})::Bool where {
        F <: MOI.ScalarAffineFunction,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
    }
    return true
end

function nrows(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Number of rows to add
    return length(func)
end

function ncoefs(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Total number of non-zero coefficients across all rows
    return sum(map(f -> length(f.terms), func))
end

function rowtypes(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Type of the new rows
    return map(f -> begin
            if S <: MOI.EqualTo
                return 'E'
            end

            if S <: MOI.GreaterThan
                return 'G'
            end

            if S <: MOI.LessThan
                return 'L'
            end
        end,
        func
    )
end

function rhs(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Type{P} array of length `nrows` containing the right hand side elements
    return map(s -> begin
            if S <: MOI.EqualTo
                return Precision(s.value)
            end

            if S <: MOI.GreaterThan
                return Precision(s.lower)
            end

            if S <: MOI.LessThan
                return Precision(s.upper)
            end
        end,
        set
    )
end

function starts(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Start indices for each row in the coefficient arrays
    start_index = 0
    return vcat(0, map(f -> start_index += length(f.terms), func))
end

function colinds(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Column indices for the coefficients (0-indexed) (concatenated for each rows)
    return vcat(
        map(
            f -> begin
                # Get XPRS indices of f's terms
                return map(t -> _XPRS_variable_from_moi_index(model, t.variable).xprs_index, f.terms)
            end,
            func
        )... # Expand outer vector into varargs for vcat
    )
end

function rowcoeffs(model::Optimizer, func::Vector{<: MOI.ScalarAffineFunction}, set::Vector{S}) where {
    S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
}
    # Coefficient values corresponding to colind
    return vcat(
        map(
            f -> begin
                # Get coefs of f's terms
                return map(t -> Precision(t.coefficient), f.terms)
            end,
            func
        )... # Expand outer vector into varargs for vcat
    )
end

function MOI.add_constraints(model::Optimizer, func::Vector{F}, set::Vector{S})::Vector{MOI.ConstraintIndex{F,S}} where {
        F <: MOI.ScalarAffineFunction,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
    }

    if length(func) != length(set)
        throw(MOI.NotAllowedError("Cannot add constraint with |func| != |set|"))
    end

    # Add function in canonical form
    for f in func
        if !MOI.Utilities.is_canonical(f)
            MOI.Utilities.canonicalize!(f)
        end
    end

    old_n = XPRSgetattrib(model.inner, "ROWS")
    indices = map(
        # Generate new MOI.ConstraintIndex{MOI.ScalarAffineFunction, ...}
        i -> begin
            # Generate MOI index by adding dummy constraint
            return _XPRS_register_new_constraint(model, old_n + (i-1), func[i], set[i])
        end,
        # Range
        1:length(func)
    )

    XPRSaddrows64(
        model.inner,
        nrows(model, func, set),
        ncoefs(model, func, set),
        rowtypes(model, func, set),
        rhs(model, func, set),
        nothing,
        starts(model, func, set),
        colinds(model, func, set),
        rowcoeffs(model, func, set)
    )
    return indices
end

function MOI.add_constraint(model::Optimizer, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.ScalarAffineFunction,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
    }
    return MOI.add_constraints(model, [func], [set])[1]
end

function MOI.supports_constraint(model::Optimizer, ::Type{F}, ::Type{S})::Bool where {
        F <: MOI.VariableIndex,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo, MOI.Interval, MOI.ZeroOne, MOI.Integer, MOI.Semicontinuous, MOI.Semiinteger}
    }
    return true
end

function MOI.add_constraints(model::Optimizer, func::Vector{F}, set::Vector{S})::Vector{MOI.ConstraintIndex{F,S}} where {
        F <: MOI.VariableIndex,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo, MOI.Interval, MOI.ZeroOne, MOI.Integer, MOI.Semicontinuous, MOI.Semiinteger}
    }

    if length(func) != length(set)
        throw(MOI.NotAllowedError("Cannot add constraint with |func| != |set|"))
    end

    # Split interval bounds into upper and lower bound calls
    if S <: MOI.Interval
        return map(
            i -> begin
                lb = MOI.add_constraint(model, func[i], MOI.GreaterThan(set[i].lower))
                ub = MOI.add_constraint(model, func[i], MOI.LessThan(set[i].upper))
                return _XPRS_register_new_constraint_as_parent(model, func[i], set[i], [lb, ub])
            end,
            1:length(func)
        )
    end

    # Retrieve variables info from the MOI indices
    variables = _XPRS_variable_from_moi_index.(model, func)
    # Convert bounds (that might even be int or bool) into the solver's desired precision (see below ifs)
    bounds = map(x -> Precision(Inf), set)
    # Deduce bounds senses from MOI scalar set type
    senses = map(x -> 'U', set)
    # Columns types (default is continuous)
    coltypes = map(x -> 'C', set)
    change_bounds = change_type = false

    if S <: MOI.EqualTo
        change_bounds = true
        bounds = map(x -> Precision(x.value), set)
        senses = map(x -> 'B', set)
    end

    if S <: MOI.GreaterThan
        change_bounds = true
        bounds = map(x -> Precision(x.lower), set)
        senses = map(x -> 'L', set)
    end

    if S <: MOI.LessThan
        change_bounds = true
        bounds = map(x -> Precision(x.upper), set)
        senses = map(x -> 'U', set)
    end

    if S <: MOI.ZeroOne
        model.has_integer_variables = true
        change_type = true
        coltypes = map(x -> 'B', set)
    end

    if S <: MOI.Integer
        model.has_integer_variables = true
        change_type = true
        coltypes = map(x -> 'I', set)
    end

    if S <: MOI.Semicontinuous
        model.has_integer_variables = true
        change_type = true
        coltypes = map(x -> 'S', set)
    end

    if S <: MOI.Semiinteger
        model.has_integer_variables = true
        change_type = true
        coltypes = map(x -> 'R', set)
    end

    # Since we are just updating bounds of existing variables, no constraints is added to the model
    if change_bounds
        XPRSchgbounds(
            model.inner,
            length(func),
            # Indices for the variables in Xpress matrix
            map(v -> v.xprs_index, variables),
            senses,
            bounds
        )
    end

    # Also change variable type if needed
    if change_type
        XPRSchgcoltype(
            model.inner,
            length(func),
            # Indices for the variables in Xpress matrix
            map(v -> v.xprs_index, variables),
            coltypes
        )
    end
    return map(
        i -> begin
            index = _XPRS_register_new_constraint(model, -2, func[i], set[i])
            return index
        end,
        1:length(func)
    )
end

function MOI.add_constraints(model::Optimizer, func::Vector{F}, set::S)::Vector{MOI.ConstraintIndex{F,S}} where {
        F <: MOI.VariableIndex,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo, MOI.Interval, MOI.ZeroOne, MOI.Integer, MOI.Semicontinuous, MOI.Semiinteger}
    }
    return MOI.add_constraints(model, func, map(s -> set, func))
end

function MOI.add_constraint(model::Optimizer, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.VariableIndex,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo, MOI.Interval, MOI.ZeroOne, MOI.Integer, MOI.Semicontinuous, MOI.Semiinteger}
    }
    return MOI.add_constraints(model, [func], [set])[1]
end

function MOI.supports_constraint(model::Optimizer, ::Type{F}, ::Type{S})::Bool where {
        F <: MOI.VectorOfVariables,
        S <: Union{MOI.SOS1, MOI.SOS2}
    }
    return true
end

# SOS1 and SOS2
# https://jump.dev/MathOptInterface.jl/stable/manual/constraints/#Discrete-and-logical-constraints
function MOI.add_constraints(model::Optimizer, func::Vector{F}, set::Vector{S})::Vector{MOI.ConstraintIndex{F,S}} where {
        F <: MOI.VectorOfVariables,
        S <: Union{MOI.SOS1, MOI.SOS2}
    }

    if length(func) != length(set)
        throw(MOI.NotAllowedError("Cannot add constraint with |func| != |set|"))
    end

    # Number of new sets
    nsets = length(func)

    # Number of new nonzeros in the added sets
    nelems = sum(f -> length(f.variables), func)

    # Character array of length nsets containing the set types: 1 indicates a SOS1; 2 indicates a SOS2
    settypes = Char[]
    if S <: MOI.SOS1
        settypes = map(s -> '1', set)
    end
    if S <: MOI.SOS2
        settypes = map(s -> '2', set)
    end

    # Start indices for each row in the coefficient arrays
    start_index = 0
    starts = vcat(0, map(f -> start_index += length(f.variables), func))

    # Column indices for the reference value (0-indexed) (concatenated for each rows)
    colinds = vcat(
        map(
            f -> begin
                # Get XPRS indices of f's terms
                return map(v -> _XPRS_variable_from_moi_index(model, v).xprs_index, f.variables)
            end,
            func
        )... # Expand outer vector into varagrs for vcat
    )

    # Reference values corresponding to colind
    refvals = vcat(
        map(
            s -> begin
                # Get coefs of f's terms
                return map(w -> Precision(w), s.weights)
            end,
            set
        )... # Expand outer vector into varagrs for vcat
    )

    XPRSaddsets64(
        model.inner,
        nsets,
        nelems,
        settypes,
        starts,
        colinds,
        refvals
    )

    return map(i -> _XPRS_register_new_constraint(model, -2, func[i], set[i]), 1:length(func))
end

function MOI.add_constraint(model::Optimizer, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.VectorOfVariables,
        S <: Union{MOI.SOS1, MOI.SOS2}
    }
    return MOI.add_constraints(model, [func], [set])[1]
end

# Vector Objective of variables: Min/Max [x1,...,xn]
function MOI.supports(::Optimizer, ::MOI.ObjectiveFunction{F})::Bool where {
        F <: MOI.VectorOfVariables
    }
    return true
end

function MOI.set(model::Optimizer, ::MOI.ObjectiveFunction{F}, set::F) where {
        F <: MOI.VectorOfVariables
    }
    return map(
        i -> begin
            # v -> 1v + 0
            t::MOI.ScalarAffineTerm{Precision} = MOI.ScalarAffineTerm(1.0, set.variables[i])
            f::MOI.ScalarAffineFunction{Precision} = MOI.ScalarAffineFunction([t], 0.0)
            _add_linear_objective(model, i-1, f)
            return MOI.VariableIndex(-1)
        end,
        1:length(set.variables)
    )
