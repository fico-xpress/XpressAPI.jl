function _reverse_polish!(model::Optimizer, f, stack)
    if  typeof(f) <: MOI.ScalarNonlinearFunction
        # Special case for unary opertarors '-' and '+'
        # -f -> [f, -]
        # +f -> [f]
        if (f.head == :(-) || f.head == :(+)) && length(f.args) == 1
            _reverse_polish!(model, f.args[1], stack)
            if f.head == :(-)
                push!(stack, :uminus)
            end
        end

        # First, find if `f` need brackets and arguments
        # This is the case for user-defined operators and elementary functions
        if !(f.head in keys(XPRS_ARITHMETIC_OPERATORS))
            # To add user-defined (or elementary function), we first need to generate tokens for operator argument separated by commas
            # For an operator like my_op(x,y), Xpress expect the following syntax:
            # ['my_op', '{', 'x', ',', 'y', '}']
            # But in Reverse Polish Notation its:
            # ['}', 'y', ',', 'x', 'my_op'] (Note: No opening brackets)
            # See https://www.fico.com/fico-xpress-optimization/docs/latest/solver/nonlinear/HTML/chapFormulae.html
            # cos(x) -> ['}', 'x', 'cos']
            # cos(x + 5) -> ['}', 'x', '5', '+', 'cos']
            # my_op(x+5, y) -> ['my_op', '{', 'x', '+', '5', ',', 'y', '}'] -> ['}', 'y', ',', 'x', '5', '+', 'my_op']
            op_args_tmp = map(
                arg -> begin
                    buffer = []
                    _reverse_polish!(model, arg, buffer)
                    return buffer
                end,
                f.args
            )
            # Reverse arguments as we are in Reverse Polish Notation
            reverse!(op_args_tmp)
            # Add delimiter between operators
            i = 1
            op_args = []
            for op_arg in op_args_tmp
                push!(op_args, op_args_tmp[i]...)
                if i > 1
                    push!(op_args, Symbol(','))
                end
                i += 1
            end
            # Finally, put all tokens in the global stack
            push!(stack, vcat(Symbol('}'), op_args, f.head)...)
        # Simply compute RPN of `f` recursively
        else
            # We this loop because g = f1+f2+f3 get translated to [f1,f2,+,f3,+]
            i = 1
            for arg in f.args
                _reverse_polish!(model, arg, stack)
                if i > 1
                    push!(stack, f.head)
                end
                i += 1
            end
        end
    end

    if typeof(f) <: Number
        push!(stack, f)
    end

    if typeof(f) <: MOI.VariableIndex
        # x -> [x]
        push!(stack, _XPRS_variable_from_moi_index(model, f))
    end

    if typeof(f) <: MOI.ScalarQuadraticTerm
        # axy -> [a, x, *, y, *]
        # y^2 and y*y is translated to 2yy by MOI/JuMP !
        coef_scaling = (f.variable_1.value == f.variable_2.value) ? 0.5 : 1.0
        push!(stack, f.coefficient * coef_scaling)
        push!(stack, _XPRS_variable_from_moi_index(model, f.variable_1))
        push!(stack, :(*))
        push!(stack, _XPRS_variable_from_moi_index(model, f.variable_2))
        push!(stack, :(*))
    end

    if typeof(f) <: MOI.ScalarAffineTerm
        # ax -> [a, x, *]
        # Simplyfication: 1x -> [1, x , *] -> [x]
        if f.coefficient == 1.0
            push!(stack, _XPRS_variable_from_moi_index(model, f.variable))
        else
            push!(stack, f.coefficient)
            push!(stack, _XPRS_variable_from_moi_index(model, f.variable))
            push!(stack, :(*))
        end
    end

    if typeof(f) <: MOI.ScalarQuadraticFunction
        i = 1
        for t in f.quadratic_terms
            _reverse_polish!(model, t, stack)
            if i > 1
                push!(stack, :(+))
            end
            i += 1
        end

        # Add the linear part, if any
        if length(f.affine_terms) > 0
            for t in f.affine_terms
                _reverse_polish!(model, t, stack)
                push!(stack, :(+))

            end
        end

        if f.constant != 0.0
            push!(stack, f.constant)
            # Sometimes MOI creates Scalar function for just constant values
            if length(f.quadratic_terms) > 0 && length(f.affine_terms) > 0
                push!(stack, :(+))
            end
        end
    end

    if typeof(f) <: MOI.ScalarAffineFunction
        i = 1
        for t in f.terms
            _reverse_polish!(model, t, stack)
            if i > 1
                push!(stack, :(+))
            end
            i += 1
        end

        if f.constant != 0.0
            push!(stack, f.constant)
            # Sometimes MOI creates Scalar function for just constant values
            if length(f.terms) > 0
                push!(stack, :(+))
            end
        end
    end
end

# Get the reverse Polish representation of 'f' as a vector of variables, operators, and constants
function reverse_polish(model::Optimizer, f)::Vector
    stack = Vector{Any}()
    _reverse_polish!(model, f, stack)
    return stack
end

# Mapping arithmetic symbols to Xpress tokens
const XPRS_ARITHMETIC_OPERATORS = Dict(
    # OP_UMINUS
    :(+) => (XPRS_TOK_OP, XPRS_OP_PLUS),
    :(-) => (XPRS_TOK_OP, XPRS_OP_MINUS),
    :uminus => (XPRS_TOK_OP, XPRS_OP_UMINUS),
    :(^) => (XPRS_TOK_OP, XPRS_OP_EXPONENT),
    :(*) => (XPRS_TOK_OP, XPRS_OP_MULTIPLY),
    :(/) => (XPRS_TOK_OP, XPRS_OP_DIVIDE)
)


# Mapping elementary functions to Xpress tokens
const XPRS_IFUNS = Dict(
    :log => (XPRS_TOK_IFUN, XPRS_IFUN_LN),
    :log10 => (XPRS_TOK_IFUN, XPRS_IFUN_LOG10),
    :exp => (XPRS_TOK_IFUN, XPRS_IFUN_EXP),
    :abs => (XPRS_TOK_IFUN, XPRS_IFUN_ABS),
    :sqrt => (XPRS_TOK_IFUN, XPRS_IFUN_SQRT),
    :sin => (XPRS_TOK_IFUN, XPRS_IFUN_SIN),
    :cos => (XPRS_TOK_IFUN, XPRS_IFUN_COS),
    :tan => (XPRS_TOK_IFUN, XPRS_IFUN_TAN),
    :asin => (XPRS_TOK_IFUN, XPRS_IFUN_ARCSIN),
    :acos => (XPRS_TOK_IFUN, XPRS_IFUN_ARCCOS),
    :atan => (XPRS_TOK_IFUN, XPRS_IFUN_ARCTAN),
    :min => (XPRS_TOK_IFUN, XPRS_IFUN_MIN),
    :max => (XPRS_TOK_IFUN, XPRS_IFUN_MAX),
    :sign => (XPRS_TOK_IFUN, XPRS_IFUN_SIGN),
    # SpecialFunctions.jl
    :erf => (XPRS_TOK_IFUN, XPRS_IFUN_ERF),
    :erfc => (XPRS_TOK_IFUN, XPRS_IFUN_ERFC)
    # IFUN_PWL
)

# Mapping user-defined function symbols to Xpress tokens
const XPRS_FUNCTIONS_SYMBOLS = Dict(
    Symbol('{') => (XPRS_TOK_LB, 0),
    Symbol(',') => (XPRS_TOK_DEL, XPRS_DEL_COMMA),
    # the colon is there for userfunctions that go to R^n so that you can say use the i-th output dimension of my user function in this formula
    Symbol(';') => (XPRS_TOK_DEL, XPRS_DEL_COLON),
    Symbol('}') => (XPRS_TOK_RB, 0)
)


function MOI.get(
    model::Optimizer,
    attr::MOI.ListOfSupportedNonlinearOperators,
)::Vector{Symbol}
    # Sanity check
    _initialize_nlp(model)
    # Get XPRS expression symbol AND user defined operators
    return vcat(keys(XPRS_IFUNS), MOI.get(model.nlp_model, attr))
end

# Given a non-linear fonction 'f'
# returns the arrays 'type' and 'value' expected by function like 'XPRSnlpaddformulas'
function get_types_and_values(model, tokens)
    type = map(
        t -> begin
            if typeof(t) <: Variable
                return XPRS_TOK_COL
            end
            if typeof(t) <: Symbol
                # Case 1, 't' is an arithmetic operator
                xprs_type, xprs_value = haskey(XPRS_ARITHMETIC_OPERATORS, t) ? XPRS_ARITHMETIC_OPERATORS[t] : (nothing, nothing)
                if !isnothing(xprs_type) return xprs_type end
                # Case 2, 't' is an elementary function
                xprs_type, xprs_value = haskey(XPRS_IFUNS, t) ? XPRS_IFUNS[t] : (nothing, nothing)
                if !isnothing(xprs_type) return xprs_type end
                # Case 3, 't' is a function symbol
                xprs_type, xprs_value = haskey(XPRS_FUNCTIONS_SYMBOLS, t) ? XPRS_FUNCTIONS_SYMBOLS[t] : (nothing, nothing)
                if !isnothing(xprs_type) return xprs_type end
                # Case 4, 't' is a user-defined operator
                return XPRS_TOK_FUN
            end
            # Constant
            return XPRS_TOK_CON
        end,
        tokens
    )

    value = map(
        t -> begin
            if typeof(t) <: Variable
                return t.xprs_index
            end
            if typeof(t) <: Symbol
                # Case 1, 't' is an arithmetic operator
                xprs_type, xprs_value = haskey(XPRS_ARITHMETIC_OPERATORS, t) ? XPRS_ARITHMETIC_OPERATORS[t] : (nothing, nothing)
                if !isnothing(xprs_value) return xprs_value end
                # Case 2, 't' is an elementary function
                xprs_type, xprs_value = haskey(XPRS_IFUNS, t) ? XPRS_IFUNS[t] : (nothing, nothing)
                if !isnothing(xprs_value) return xprs_value end
                # Case 3, 't' is a function symbol
                xprs_type, xprs_value = haskey(XPRS_FUNCTIONS_SYMBOLS, t) ? XPRS_FUNCTIONS_SYMBOLS[t] : (nothing, nothing)
                if !isnothing(xprs_value) return xprs_value end
                # Case 4, 't' is a user-defined operator
                return model.user_func_mapping[t]
            end
            # Constant
            return t
        end,
        tokens
    )
    # Add the 'EOF' token and a dummy value
    push!(type, XPRS_TOK_EOF)
    push!(value, 0)
    return type, value
end

# Non-Linear constraints
function MOI.supports_constraint(model::Optimizer, ::Type{F}, ::Type{S})::Bool where {
        F <: MOI.ScalarNonlinearFunction,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
    }
    return true
end

function MOI.add_constraint(model::Optimizer, func::F, set::S)::MOI.ConstraintIndex{F,S} where {
        F <: MOI.ScalarNonlinearFunction,
        S <: Union{MOI.GreaterThan, MOI.LessThan, MOI.EqualTo}
    }
    # Sanity check
    _initialize_nlp(model)

    # Sometimes MOI will write the nl function 'f' as 'f - 0.0'
    # Here we just extract 'f'
    if func.head == :(-) && func.args[2] == 0.0 && typeof(func.args[1]) <: MOI.ScalarNonlinearFunction
        _func = func.args[1]
    else
        _func = func
    end
    # Also simplify other trival functions:
    # https://jump.dev/MathOptInterface.jl/stable/submodules/Nonlinear/SymbolicAD/#simplify
    MOI.Nonlinear.SymbolicAD.simplify!(_func)

    # Add function in canonical form
    if !MOI.Utilities.is_canonical(_func)
        MOI.Utilities.canonicalize!(_func)
    end

    index = MOI.add_constraint(model, MOI.ScalarAffineFunction{Precision}([MOI.ScalarAffineTerm{Precision}(0.0, MOI.VariableIndex(1))], 0.0), set)
    c = _XPRS_constraint_from_moi_index(model, index)

    # See https://www.fico.com/fico-xpress-optimization/docs/latest/solver/nonlinear/HTML/XSLPaddformulas.html
    # Transform the non-linear expression into a postfix (reverse Polish) stack
    tokens = reverse_polish(model, _func)

    # `ncoefs::Integer`: Number of non-linear coefficients to be added.
    ncoefs = 1

    # `rowind::AbstractVector{Integer}`: Integer array holding index of row for the coefficient.
    rowind = [c.xprs_index]

    # `formulastart::AbstractVector{Integer}`: Integer array of length `ncoefs+1` holding the start position in the arrays `type` and `value` of the formula for the coefficients.
    formulastart = [0, length(tokens) + 1]

    # Reverse Polish Notation
    parsed = 1

    # `type::AbstractVector{Integer}`: Array of token types providing the formula for each coefficient.
    # `value::AbstractVector{Number}`: Array of values corresponding to the types in `type`.
    type, value = get_types_and_values(model, tokens)

    XPRSnlpaddformulas(
        model.inner,
        ncoefs,
        rowind,
        formulastart,
        parsed,
        type,
        value
    )

    # Flag as NL constraint
    return _XPRS_register_new_constraint_as(model, c, func, set)
end

# https://jump.dev/MathOptInterface.jl/stable/reference/models/#MathOptInterface.UserDefinedFunction
# https://jump.dev/MathOptInterface.jl/stable/submodules/Nonlinear/overview/#User-defined-operators
function MOI.set(model::Optimizer, attr::MOI.UserDefinedFunction, args)
    # Function callback and its derivatives are stored in 'args'
    f = ∇f = ∇²f = nothing
    if length(args) >= 1
        f = args[1]
    end
    if length(args) >= 2
        ∇f = args[2]
    end
    # We don't actually need second-order (partial) derivatives for SLP
    if length(args) >= 3
        ∇²f = args[3]
    end

    """
    `func` is invoked with this signature
    ```
    function(values::AbstractVector{Cdouble}, deltas::AbstractVector{Cdouble}, partials::AbstractVector{Cdouble})::Tuple{error::Cint,evaluation::Cdouble}
    ```
    """
    function user_callbackfunction(values, deltas, partials)
        xprs_eval = xprs_error = 0
        if !isnothing(f)
            xprs_eval = f(values...)
        else
            xprs_error = 1
        end
        if !isnothing(∇f) && !isnothing(deltas)
            new_partials = copy(partials)
            ∇f(new_partials, values...)
            # Only return derivatives for x if deltas[x] != 0
            for i in 1:length(partials)
                partials[i] = deltas[i] == 0.0 ? partials[i] : new_partials[i]
            end
        end
        return xprs_error, xprs_eval
    end

    # map: R->R
    # vecmap: R^n->R
    # multimap: R^n->R^n
    # +'delta' for derivatives
    # Register the operator as an Xpress user-defined function
    # This will generate a token-id that we will add in `XPRSnlpaddformulas` with MOI.ScalarNonlinearFunction constraint
    token_id = XPRSnlpadduserfunctionvecmapdelta(
        model.inner,
        # Function name
        "$(attr.name)",
        # Input dimension
        attr.arity,
        # Output dimension is assumed '1' here
        # options
        XPRS_NLPINSTANCEFUNCTION,
        # Callback
        user_callbackfunction
    )

    # Sanity check
    _initialize_nlp(model)

    # Add the new operator in MOI
    MOI.Nonlinear.register_operator(
        model.nlp_model,
        attr.name,
        attr.arity,
        args...,
    )

    # Register new user-function token_id
    model.user_func_mapping[attr.name] = token_id
end
